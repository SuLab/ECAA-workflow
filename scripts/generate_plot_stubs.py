#!/usr/bin/env python3
"""Generate synthetic input stubs for the L2 per-atom plot smoke test.

Reads ``runtime/plot-atoms-enumeration.json`` (generated by
``enumerate_plot_atoms.py``) and writes one input directory per atom under
``testdata/plot-stubs/<atom_id>/input/``.

Each stub directory contains:
  - ``manifest.json``  — minimal manifest that satisfies the renderer.
  - One or more ``*.tsv`` files   — synthetic numeric data, 50 rows.

The synthetic data columns are inferred from the renderer module's
doc-comment "Expected inputs" section and from well-known filename
conventions (e.g. ``de_table.tsv``, ``qc_metrics.tsv``).

Renderers are tolerant by design: they fall through to
``FigureManifest.skipped`` rather than raising when an artifact is
absent. The stubs ensure at least one figure per stage resolves from
``written`` rather than ``skipped``.
"""
from __future__ import annotations

import json
import random
import sys
from pathlib import Path
from typing import Any

REPO_ROOT = Path(__file__).resolve().parent.parent
ENUM_FILE = REPO_ROOT / "runtime" / "plot-atoms-enumeration.json"
STUB_ROOT = REPO_ROOT / "testdata" / "plot-stubs"

N_ROWS = 50
random.seed(42)

# ---------------------------------------------------------------------------
# Column generators
# ---------------------------------------------------------------------------

def _without_advancing_rng(fn):
    state = random.getstate()
    try:
        return fn()
    finally:
        random.setstate(state)


def _floats(n: int = N_ROWS, lo: float = 0.0, hi: float = 1.0) -> list[float]:
    return [round(random.uniform(lo, hi), 6) for _ in range(n)]

def _log2fc(n: int = N_ROWS) -> list[float]:
    return [round(random.gauss(0, 2), 4) for _ in range(n)]

def _pvals(n: int = N_ROWS) -> list[float]:
    return [round(10 ** random.uniform(-10, -0.1), 8) for _ in range(n)]

def _gene_names(n: int = N_ROWS) -> list[str]:
    return [f"GENE{i:03d}" for i in range(n)]

def _sample_names(n: int = N_ROWS) -> list[str]:
    return [f"sample_{i:02d}" for i in range(n)]

def _cell_names(n: int = N_ROWS) -> list[str]:
    return [f"cell_{i:04d}" for i in range(n)]

def _chromosomes(n: int = N_ROWS) -> list[str]:
    chroms = [str(i) for i in range(1, 23)] + ["X", "Y"]
    return [random.choice(chroms) for _ in range(n)]

def _positions(n: int = N_ROWS) -> list[int]:
    return [random.randint(1_000_000, 250_000_000) for _ in range(n)]

def _clusters(n: int = N_ROWS, k: int = 8) -> list[str]:
    return [str(random.randint(0, k - 1)) for _ in range(n)]

def _celltypes(n: int = N_ROWS) -> list[str]:
    types = ["T cell", "B cell", "NK cell", "Monocyte", "Dendritic"]
    return [random.choice(types) for _ in range(n)]


def write_tsv(path: Path, columns: dict[str, list]) -> None:
    """Write a tab-separated file from a {header: values} dict."""
    path.parent.mkdir(parents=True, exist_ok=True)
    headers = list(columns.keys())
    rows = list(zip(*columns.values()))
    with open(path, "w") as f:
        f.write("\t".join(headers) + "\n")
        for row in rows:
            f.write("\t".join(str(v) for v in row) + "\n")


def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w") as f:
        json.dump(data, f, indent=2)


