def main() -> None:
    if not ENUM_FILE.exists():
        print(
            f"ERROR: {ENUM_FILE} not found. Run: python3 scripts/enumerate_plot_atoms.py "
            "> runtime/plot-atoms-enumeration.json",
            file=sys.stderr,
        )
        sys.exit(1)

    with open(ENUM_FILE) as f:
        atoms = json.load(f)

    written = 0
    skipped = 0
    for atom in atoms:
        atom_id = atom["atom_id"]
        stage_id = atom["stage_id"]
        stub_dir = STUB_ROOT / atom_id / "input"

        writer = _STUB_MAP.get(stage_id)
        if writer is None:
            print(f"  SKIP  {atom_id}: no stub writer for stage_id={stage_id}", file=sys.stderr)
            skipped += 1
            continue

        stub_dir.mkdir(parents=True, exist_ok=True)
        try:
            writer(stub_dir)
            print(f"  OK    {atom_id}  ({stage_id})")
            written += 1
        except Exception as e:
            print(f"  ERROR {atom_id}: {e}", file=sys.stderr)

    print(f"\nGenerated stubs: {written}, skipped (no writer): {skipped}", file=sys.stderr)


if __name__ == "__main__":
    main()
